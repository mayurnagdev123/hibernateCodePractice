package com.shinchan.hibernateannotationspractice;

/**
 *
 * @author mnagdev
 */
import javax.persistence.*;
@Entity
@Table(name="owner")
public class Owner {
    @Id
    private int aadhar;
    private String name;

    public int getAadhar() {
        return aadhar;
    }

    public void setAadhar(int aadhar) {
        this.aadhar = aadhar;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    
    
    
    
    
}
